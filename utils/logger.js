const winston = require('winston');
const path = require('path');

// Definimos el formato de los logs
const logFormat = winston.format.combine(
  winston.format.timestamp({ format: 'YYYY-MM-DD HH:mm:ss' }),
  winston.format.errors({ stack: true }),
  winston.format.splat(),
  winston.format.json()
);

// Opciones comunes para rotación de archivos
const fileOptions = {
  maxsize: 5 * 1024 * 1024, // 5MB por archivo
  maxFiles: 5,               // Mantener 5 archivos rotados
  tailable: true,
};

const logger = winston.createLogger({
  level: 'info',
  format: logFormat,
  defaultMeta: { service: '4fun-backend' },
  transports: [
    new winston.transports.File({ 
      filename: path.join(__dirname, '../logs/error.log'), 
      level: 'error',
      ...fileOptions
    }),
    new winston.transports.File({ 
      filename: path.join(__dirname, '../logs/combined.log'),
      ...fileOptions
    })
  ]
});

// Si no estamos en producción, también mostramos logs en la consola con colores
if (process.env.NODE_ENV !== 'production') {
  logger.add(new winston.transports.Console({
    format: winston.format.combine(
      winston.format.colorize(),
      winston.format.simple()
    )
  }));
}

module.exports = logger;
